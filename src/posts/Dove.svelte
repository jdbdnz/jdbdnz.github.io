      <time datetime="2020-11-12" class="text-tertiary text-sm">November 12th, 2020</time>
      <h2 class="text-2xl mb-1">Dove, a short story</h2>
    </header>
    <p class="my-4">
      To stare at my feet, and at the cracked earth which partially crumbles to sand where my feet fall, is a bad habit.
      I am compelled to keep my head up, searching the indistinct horizon for the Wild which I was threatened with since
      youth.
    </p>
    <p class="my-4">Uncivilized people, without code or creed, or houses.</p>
    <p class="my-4">
      Subtract from us our hab dome, the closed loop recycling systems and our manicured environment, would we be
      indistinguishable?
    </p>
    <p class="my-4">Am I, outside my hab dome, now Wild?</p>
    <p class="my-4">Where the dark blue of our weakened atmosphere meets lumpy tan hills is uniformly disappointing.</p>
    <p class="my-4">
      I saw my second hab dome from a distance this morning. My third if you count what I once called my home.
    </p>
    <p class="my-4">
      They’re not meant to be there, we were told we were the only one, and warned not to leave since outside there is
      only the Wild and the Wild is dangerous.
    </p>
    <p class="my-4">
      However the assaults I endure come not from without but from within. It is thirst that is killing me, not the
      Wild.
    </p>
    <p class="my-4">I thought to assimilate with them. If they live, then why not I?</p>
    <p class="my-4">
      But they do not seem to live. Instead, all I find are these bloody hab domes which should not be there.
    </p>
    <p class="my-4">
      I’ve skirted well beyond their surveillance zones so far, but unless I catch a break soon I’ll need to approach
      the next one. I need water.
    </p>
    <p class="my-4">
      Will they take me? Could I slip in unnoticed perhaps? I really don’t want to do this, I don’t want to go back. My
      neighbors jeering faces loom raw in my memory. Easter’s especially. Yearning mixed with horror - but I mustn’t
      think about that.
    </p>
    <p class="my-4">
      My cream shoes. Their brown coat of dust. No, the horizon. What’s there? Nothing. God I’m thirsty.
    </p>
    <p class="my-4">
      What if there are no more? Should I turn back? Or is this the demarcation, and soon I’ll be with the Wild?
      Continuing could kill me. Turning back could kill me.
    </p>
    <p class="my-4">
      The small pebbles scattered around my feet cast long shadows. Is there a pattern? What would they be trying to
      communicate?
    </p>
    <p class="my-4">
      I am going to rest for a bit. If I wake up, I won’t be dead, and I will continue if I can rekindle the hope in my
      heart.
    </p>
    <p class="my-4">*&nbsp;*&nbsp;*</p>
    <p class="my-4">
      When I wake, the sunrise is shooting curling waves of deep pinks and oranges through the eastern sky. It is
      achingly dry.
    </p>
    <p class="my-4">The wind picked up in the night and I cannot see my footprints.</p>
    <p class="my-4">Which direction did I come from? Could the sun tell me?</p>
    <p class="my-4">
      It may, but does not. Instead it shows me a glint on the horizon, my salvation. Or my death. I walk toward it.
    </p>
    <p class="my-4">
      It hurts to walk. The glint resolves into the great translucent thermoplastic polyurethane roof of a hab dome.
      Mine? Fuck they all look the same. They’ll have seen me now. I should have come in darkness where I would have had
      a chance to slip in unnoticed.
    </p>
    <p class="my-4">
      It’s too late for that plan. I’m approaching the entrance and I can see them now too. Peering out at me,
      bewildered. I’m collapsing. Please help me.
    </p>
    <p class="my-4">
      I hear shouting. The word Wild echoes around me, I cannot move. I want to explain. I cannot speak. Meanwhile,
      their voices are becoming frenetic. Shut up.
    </p>
    <p class="my-4">A rock hits me in the leg. Really?</p>
    <p class="my-4">Another, bigger, on my shoulder. I curl up, protecting my head. More stones.</p>
    <p class="my-4">
      I wonder what they will do with my body. Will I be recycled as we do all our dead? Or would that destabilize a
      balanced system, and I will be left to rot here on their doorstep? I suppose it is not for me to know.
    </p>
